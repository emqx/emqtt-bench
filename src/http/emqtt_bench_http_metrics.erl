-module(emqtt_bench_http_metrics).

-export([ init/2
        , allowed_methods/2
        , content_types_provided/2
        , text/2
        ]).

init(Req, State) ->
    {cowboy_rest, Req, State}.

allowed_methods(Req, State) ->
    {[<<"GET">>], Req, State}.

content_types_provided(Req, State) ->
    { [{<<"text/plain">>, text}]
    , Req
    , State
    }.

text(Req, State) ->
    Body = prometheus_text_format:format(),
    {Body, Req, State}.
